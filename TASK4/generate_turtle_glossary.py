def add_card(doc, label, label_color, cn_name, en_name, body):
    """Add a concept card as bordered table"""
    tbl = doc.add_table(rows=1, cols=1)
    tbl.alignment = WD_TABLE_ALIGNMENT.LEFT
    cell = tbl.cell(0, 0)
    for side in ["top", "left", "bottom", "right"]:
        cell.margin_top = Cm(0.3)
        cell.margin_bottom = Cm(0.3)
        cell.margin_left = Cm(0.4)
        cell.margin_right = Cm(0.4)

    cell.paragraphs[0].clear()
    p = cell.paragraphs[0]

    r1 = p.add_run(f"\u3010{label}\u3011")
    r1.font.size = Pt(9)
    r1.font.bold = True
    r1.font.color.rgb = label_color

    r2 = p.add_run(f" {cn_name}")
    r2.font.size = Pt(12)
    r2.font.bold = True
    r2.font.color.rgb = RGBColor(44, 44, 42)

    if en_name:
        r3 = p.add_run(f"  ({en_name})")
        r3.font.size = Pt(9)
        r3.font.color.rgb = RGBColor(136, 135, 128)

    p2 = cell.add_paragraph()
    p2.paragraph_format.space_before = Pt(2)
    p2.paragraph_format.space_after = Pt(4)
    r4 = p2.add_run("\u2500" * 60)
    r4.font.size = Pt(5)
    r4.font.color.rgb = RGBColor(180, 178, 169)

    p3 = cell.add_paragraph()
    p3.paragraph_format.space_after = Pt(2)
    p3.paragraph_format.line_spacing = 1.6

    # Parse body for `highlighted` text
    segments = body.split("`")
    for idx, seg in enumerate(segments):
        r = p3.add_run(seg)
        r.font.size = Pt(10)
        if idx % 2 == 1:
            r.font.bold = True
            r.font.color.rgb = RGBColor(153, 28, 29)
        else:
            r.font.color.rgb = RGBColor(68, 68, 65)

    # Border
    tc_pr = cell._tc.get_or_add_tcPr()
    borders = OxmlElement("w:tcBorders")
    for bn in ["top", "left", "bottom", "right"]:
        b = OxmlElement(f"w:{bn}")
        b.set(qn("w:val"), "single")
        b.set(qn("w:sz"), "6")
        b.set(qn("w:space"), "4")
        b.set(qn("w:color"), "C0C0C0")
        borders.append(b)
    tc_pr.append(borders)

    doc.add_paragraph()


def add_formula(doc, text):
    p = doc.add_paragraph()
    p.paragraph_format.space_before = Pt(4)
    p.paragraph_format.space_after = Pt(4)
    r = p.add_run(f"  {text}")
    r.font.size = Pt(10)
    r.font.name = "Consolas"
    r.font.color.rgb = RGBColor(60, 52, 137)
    pPr = p._p.get_or_add_pPr()
    shd = OxmlElement("w:shd")
    shd.set(qn("w:fill"), "F1EFE8")
    shd.set(qn("w:val"), "clear")
    pPr.append(shd)


def add_note(doc, text):
    p = doc.add_paragraph()
    p.paragraph_format.space_before = Pt(4)
    p.paragraph_format.space_after = Pt(4)
