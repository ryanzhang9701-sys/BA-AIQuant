def add_warning(doc, title, text):
    tbl = doc.add_table(rows=1, cols=1)
    tbl.alignment = WD_TABLE_ALIGNMENT.LEFT
    cell = tbl.cell(0, 0)
    cell.margin_top = Cm(0.3)
    cell.margin_bottom = Cm(0.3)
    cell.margin_left = Cm(0.4)
    cell.margin_right = Cm(0.4)

    cell.paragraphs[0].clear()
    pt = cell.paragraphs[0]
    rt = pt.add_run(f"\u26a0 {title}")
    rt.font.size = Pt(10.5)
    rt.font.bold = True
    rt.font.color.rgb = RGBColor(153, 28, 29)

    pb = cell.add_paragraph()
    rb = pb.add_run(text)
    rb.font.size = Pt(9.5)
    rb.font.color.rgb = RGBColor(113, 43, 19)

    tc_pr = cell._tc.get_or_add_tcPr()
    shd = OxmlElement("w:shd")
    shd.set(qn("w:fill"), "FAEEDA")
    shd.set(qn("w:val"), "clear")
    tc_pr.append(shd)

    borders = OxmlElement("w:tcBorders")
    for bn in ["top", "left", "bottom", "right"]:
        b = OxmlElement(f"w:{bn}")
        b.set(qn("w:val"), "single")
        b.set(qn("w:sz"), "6")
        b.set(qn("w:space"), "4")
        b.set(qn("w:color"), "BA7517")
        borders.append(b)
    tc_pr.append(borders)

    doc.add_paragraph()


